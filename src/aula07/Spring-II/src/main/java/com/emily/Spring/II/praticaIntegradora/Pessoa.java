package com.emily.Spring.II.praticaIntegradora;

public class Pessoa {
    int id;
    String name;
    String lastname;
    Integer age;

    public Pessoa(int id, String name, String lastname, Integer age) {
        this.id = id;
        this.name = name;
        this.lastname = lastname;
        this.age = age;
    }
}
